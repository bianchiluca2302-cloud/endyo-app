/**
 * useTagTranslator() → translateTag(tag: string) → string
 * Translates any AI-generated season/occasion tag to the current UI language,
 * regardless of which language it was saved in.
 */
export function useTagTranslator() {
  const language = useSettingsStore(s => s.language || 'it')
  return (tag) => {
    if (!tag) return tag
    const entry = TAG_MAP[tag.toLowerCase()]
    if (!entry) return tag  // unknown tag — return as-is
    return entry[language] || entry.it
  }
}

/**
 * useT() → t(key, ...args)
 * Hook React: legge automaticamente la lingua dallo store delle impostazioni.
 * Se la stringa è una funzione (es. outfitsSaved), la chiama con gli args.
 */
export function useT() {
  const language = useSettingsStore(s => s.language)
  const lang = translations[language] || translations.it
  return (key, ...args) => {
    const val = lang[key]
    if (val === undefined) return key
    if (typeof val === 'function') return val(...args)
    return val
  }
}

